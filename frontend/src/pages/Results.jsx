import { useLocation, useNavigate } from 'react-router-dom';
import { useAuditHistory } from '../context/AuditHistoryContext.jsx';

const STATUS_LABELS = {
  normal: 'NORMAL',
  watch: 'WATCH',
  anomaly: 'ANOMALY',
};

function formatCurrency(amount, currency) {
  return new Intl.NumberFormat('en-GB', {
    currency,
    maximumFractionDigits: 0,
    style: 'currency',
  }).format(amount);
}

function getFlaggedSources(result) {
  if (result.status === 'normal') {
    return new Set();
  }

  const flagged = new Set();

  result.discrepancies.forEach((discrepancy) => {
    if (discrepancy.direction.includes('crm')) {
      flagged.add('crm');
    }

    if (discrepancy.direction.includes('erp')) {
      flagged.add('erp');
    }

    if (discrepancy.direction.includes('finance')) {
      flagged.add('finance');
    }
  });

  return flagged;
}

function SystemData({ result }) {
  const systems = Object.entries(result.system_data);
  const maxAmount = Math.max(...systems.map(([, system]) => system.amount));
  const flaggedSources = getFlaggedSources(result);

  return (
    <section className="result-section">
      <h3>System Data</h3>
      <div className="system-data-grid">
        {systems.map(([key, system]) => {
          const isFlagged = flaggedSources.has(key);
          const barWidth = `${Math.max((system.amount / maxAmount) * 100, 8)}%`;

          return (
            <div
              className={`system-data-card ${isFlagged ? 'system-data-warning' : ''}`}
              key={key}
            >
              <div className="system-data-topline">
                <span>{system.label}</span>
                <small>{system.field}</small>
              </div>
              <strong>{formatCurrency(system.amount, system.currency)}</strong>
              <div className="amount-bar" aria-hidden="true">
                <span style={{ width: barWidth }} />
              </div>
            </div>
          );
        })}
      </div>
    </section>
  );
}

function Discrepancies({ discrepancies }) {
  return (
    <section className="result-section">
      <h3>Discrepancies</h3>
      {discrepancies.length === 0 ? (
        <p className="no-discrepancies">
          <span aria-hidden="true">✓</span>
          No discrepancies found. All systems consistent.
        </p>
      ) : (
        <div className="discrepancy-list">
          {discrepancies.map((discrepancy) => (
            <div className="discrepancy-row" key={discrepancy.field_pair}>
              <span>{discrepancy.field_pair}</span>
              <strong>{discrepancy.difference}</strong>
              <small>{discrepancy.direction}</small>
            </div>
          ))}
        </div>
      )}
    </section>
  );
}

function RootCause({ rootCause }) {
  return (
    <section className="result-section root-cause-block">
      <h3>Root-Cause Analysis</h3>
      <div>
        <span className="result-label">Probable cause</span>
        <p>{rootCause.probable_cause}</p>
      </div>
      <div>
        <span className="result-label">Evidence</span>
        <ul>
          {rootCause.evidence.map((item) => (
            <li key={item}>{item}</li>
          ))}
        </ul>
      </div>
      <div>
        <span className="result-label">Recommended action</span>
        <p>{rootCause.recommended_action}</p>
      </div>
    </section>
  );
}

function AiAnalysis({ text }) {
  return (
    <section className="result-section ai-analysis-block">
      <div className="ai-analysis-heading">
        <h3>AI Analysis</h3>
        <p>Generated by Root-Cause agent via Band</p>
      </div>
      <p className="ai-analysis-text">{text}</p>
    </section>
  );
}

function ResultCard({ result }) {
  const status = result.status;

  return (
    <article className="result-card">
      <header className="result-card-header">
        <div>
          <h2>{result.entity}</h2>
          <p>{result.query_id}</p>
        </div>
        <span className={`status-badge status-badge-${status}`}>
          {STATUS_LABELS[status] ?? status.toUpperCase()}
        </span>
      </header>

      <SystemData result={result} />
      <Discrepancies discrepancies={result.discrepancies} />
      <RootCause rootCause={result.root_cause} />
      <AiAnalysis text={result.ai_analysis_text} />
    </article>
  );
}

export default function Results() {
  const location = useLocation();
  const navigate = useNavigate();
  const { addHistoryResults } = useAuditHistory();
  const results = Array.isArray(location.state?.results)
    ? location.state.results.slice(0, 3)
    : [];

  const backToDashboard = () => {
    if (!location.state?.fromHistory && results.length > 0) {
      addHistoryResults(results);
    }

    navigate('/');
  };

  return (
    <main className="results-page">
      <header className="results-hero">
        <div>
          <p className="system-label">Band workflow complete</p>
          <h1>Audit Results</h1>
          <p>Reconciliation complete</p>
        </div>
        <button
          className="back-dashboard"
          type="button"
          onClick={backToDashboard}
        >
          ← Back to Dashboard
        </button>
      </header>

      {results.length === 0 ? (
        <section className="empty-results">
          <h2>No audit results available</h2>
          <p>Run an audit from the dashboard to view reconciliation output.</p>
        </section>
      ) : (
        <section
          className={`results-grid results-grid-${results.length}`}
          aria-label="Audit result windows"
        >
          {results.map((result) => (
            <ResultCard key={result.query_id} result={result} />
          ))}
        </section>
      )}
    </main>
  );
}
